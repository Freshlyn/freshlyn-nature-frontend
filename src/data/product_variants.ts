export interface ProductVariant {
  id: string;
  product_id: string;
  name: string;
  quantity_value: number;
  quantity_unit: string;
  price: number;
  stock_quantity: number;
  is_default: boolean;
}

export type SubscriptionFrequency = "daily" | "alternate" | "every_3rd";

export interface SubscriptionOption {
  // Number of deliveries in this plan, independent of frequency — e.g. "30 Deliveries"
  // always means 30 drop-offs, whether they land daily or every 2/3 days.
  duration_days: number;
  label: string;
  discount_percent: number;
}

export interface ProductSubscriptionConfig {
  product_id: string;
  enabled: boolean;
  durations: SubscriptionOption[];
  frequencies: SubscriptionFrequency[];
}

