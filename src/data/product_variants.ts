export function getVariantsForProduct(productId: string): ProductVariant[] {
  return productVariants.filter((v) => v.product_id === productId);
}

export function getDefaultVariant(
  productId: string,
): ProductVariant | undefined {
  return productVariants.find(
    (v) => v.product_id === productId && v.is_default,
  );
}

export function getVariantById(variantId: string): ProductVariant | undefined {
  return productVariants.find((v) => v.id === variantId);
}

export function getSubscriptionConfig(
  productId: string,
): ProductSubscriptionConfig | undefined {
  return subscriptionConfigs.find((c) => c.product_id === productId);
}

export function isSubscriptionEnabled(productId: string): boolean {
  return (
    subscriptionConfigs.find((c) => c.product_id === productId)?.enabled ??
    false
  );
}

export function getFrequencyLabel(frequency: SubscriptionFrequency): string {
  const labels: Record<SubscriptionFrequency, string> = {
    daily: "Everyday",
    alternate: "Every 2 days",
    every_3rd: "Every 3 days",
  };
  return labels[frequency];
}

export function getFrequencyIntervalDays(
  frequency: SubscriptionFrequency,
): number {
  const intervals: Record<SubscriptionFrequency, number> = {
    daily: 1,
    alternate: 2,
    every_3rd: 3,
  };
  return intervals[frequency];
}
